using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculadora
{
    public class Funciones
    {
        public int Sumar(int a, int b) 
        {
            int total;
            total = a + b;
            return total;
        }
        public int Multiplicar(int a, int b)
        {
            int total;
            total = a * b;
            return total;
        }
    }
