namespace Calculadora
{
    public class Funciones
    {
        public static int Sumar(int a, int b) 
        {
            int total;
            total = a + b;
            return total;
        }
        public static int Multiplicar(int a, int b)
        {
            int total;
            total = a * b;
            return total;
        }
    }
}
